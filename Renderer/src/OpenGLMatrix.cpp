#include "OpenGLMatrix.h"

#include <SDL_opengl.h>

//////////////////////////////////////////////////////////////
using Dubious::Renderer::OpenGLMatrix;

//////////////////////////////////////////////////////////////
OpenGLMatrix::OpenGLMatrix()
{
    glPushMatrix();
}

//////////////////////////////////////////////////////////////
OpenGLMatrix::~OpenGLMatrix()
{
    glPopMatrix();
}

//////////////////////////////////////////////////////////////
void OpenGLMatrix::LoadIdentity()
{
    glLoadIdentity();
}

//////////////////////////////////////////////////////////////
void OpenGLMatrix::MatrixMode( int Mode )
{
    glMatrixMode( Mode );
}

